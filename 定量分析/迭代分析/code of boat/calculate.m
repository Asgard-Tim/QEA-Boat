%此函数为根据数据给出的拟合曲面结果，通过该函数来刻画船体
function z=calculate(x,y)       
   p00 =       1.411; 
   p10 =     -0.1623;  
   p01 =   1.908e-15;  
   p20 =    0.009543; 
   p11 =   -1.56e-16; 
   p02 =      0.0687; 
   p30 =  -0.0001763;  
   p21 =   3.468e-18; 
   p12 =   -0.002936; 
   p03 =   -1.02e-17;
   p40 =    1.04e-06;
   p31 =  -2.271e-20;  
   p22 =   2.858e-05; 
   p13 =    1.89e-19;
   p04 =    0.002836;  
   z = p00 + p10*x + p01*y + p20*x^2 + p11*x*y + p02*y^2 + p30*x^3 + p21*x^2*y + p12*x*y^2 + p03*y^3 + p40*x^4 + p31*x^3*y + p22*x^2*y^2 + p13*x*y^3 + p04*y^4;
end


