%此函数为根据数据给出的拟合曲面结果，通过该函数来刻画船体
function z=calculate(x,y)       
   p00 =       3.268; 
   p10 =     -0.7873;  
   p01 =  -2.173e-16;  
   p20 =     0.06609; 
   p11 =   7.248e-17; 
   p02 =     0.09619; 
   p30 =   -0.002006;  
   p21 =  -5.316e-18; 
   p12 =   -0.008344; 
   p03 =   5.362e-18;
   p40 =    2.05e-05;
   p31 =   1.001e-19;  
   p22 =   0.0001442; 
   p13 =  -2.301e-19;
   p04 =    0.003418;  
   z = p00 + p10*x + p01*y + p20*x^2 + p11*x*y + p02*y^2 + p30*x^3 + p21*x^2*y + p12*x*y^2 + p03*y^3 + p40*x^4 + p31*x^3*y + p22*x^2*y^2 + p13*x*y^3 + p04*y^4;
end